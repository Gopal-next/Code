# Binary matrix pyramid:

#           1
#          101
#         10101
#        1010101
#       101000101
#      10101010101
#     1010101010101
#    101010101010101
#   10101000001010101
#  1010101111101010101
# 101010101010101010101


n = 11
for i in range(n):
    sp = " "* (n-i-1)
    l =""
    for j in range(2*i+1):
        if j%2 ==0:
            l += '1'
        else:
            l += '0'
    print( sp +l)
